class Summation():
    def __call__(self, a,b):
        return a+b

class Multiply():
    def __call__(self, a,b):
        return a*b 
        
class Subtraction():
    def __call__(self, a,b):
        return a-b 
    
class Division():
    def __call__(self, a,b):
        return a/b     
    
     